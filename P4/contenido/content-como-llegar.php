            <h2><i class="fa fa-car"></i> En coche</h2>
            <p class="text-right">Desde la A-44 tomar la salida 125. En la rotonda coger la salida de la Avenida de la Constitución y, más adelante, girar a la izquierda hacia la Calle Periodista Eugenio Selles. Al final de esta calle se encuentra la sede.</p>

            <h2><i class="fa fa-bus"></i> Desde la estación de autobuses</h2>
            <p> El congreso se encuentra a tan solo 10 o 15 minutos ndando desde la estación de autobuses.</p>
            <p>Para llegar solo hay que bajar la Calle Ovidio y en la rotonda ir por la salida Avenida Compositor Luis de Narváez. Tras bajar esa calle se encuentra la sede.</p>

            <h2><i class="fa fa-plane"></i> Desde el aeropuerto</h2>
            <p>Para llegar al congreso es necesario coger el autobús que parte desde el aeropuerto cada hora a partir de las 6:00 hasta las 19:00. El autobús tiene su parada en Granada en la Caleta donde se podrá hacer trasbordo y coger las líneas N3 o SN3.</p>
            <p>La línea N3 tiene una parada en la Avenida Compositor Luis de Narváez. Al final de esta calle se encuentra la sede. </p>
            <p>La línea SN3 nos permite bajar junto a la Calle Periodista Eugenio Selles. Solo hay que subir esta calle hasta llegar a la sede.</p>

            <h2><i class="fa fa-train"></i> Desde la estación de tren</h2>
            <p>Tras salir de la estación de tren hay que subir la Avenida de Andaluces hasta a la Avenida de la Constitución. Allí podemos coger la linea N3.</p>
            <p>La línea N3 tiene una parada en la Avenida Compositor Luis de Narváez. Al final de esta calle se encuentra la sede.</p>
