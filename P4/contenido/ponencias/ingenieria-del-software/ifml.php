            <h1>IFML</h1>
            <h3 id="ponente">por M. Cabrera</h3>
            <p>IFML (Interaction Flow Modeling Language) es un lenguaje de modelado estándar en el campo de la ingeniería de software. IFML incluye un conjunto de notaciones gráficas para crear modelos visuales de las interacciones del usuario y el comportamiento de aplicaciones para el usuario en los sistemas de software.
IFML fue desarrollado en 2012 y 2013 bajo la dirección de WebRatio y fue inspirado por la notación WebML, así como por algunas otras experiencias en el campo de la modelización Web.</p>
