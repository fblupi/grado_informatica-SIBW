            <h1>Digitalización 3D, por F.J. Melero</h1>
            <p>La digitalización en 3D facilita la digitalización rápida y flexible de las dimensiones de los modelos, herramientas, prototipos y muchos otros objetos formados como desee, independientemente del tipo de material en tiempo real. Como procedimiento sin contacto la digitalización en 3D proporciona los datos exactos de la geometría de la pieza componente. Tamaño de la pieza que se va a escanear puede ser de 1 mm hasta 10 m. Para la comparación de tolerancias, modelos de volumen pueden compararse con estos datos con los datos de diseño CAD o incluso nuevas herramientas y modelos que forman pueden ser hechas.</p>
