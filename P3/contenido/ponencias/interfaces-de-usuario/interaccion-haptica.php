            <h1>Interacción Háptica</h1>
            <h3 id="ponente">por F. Soler</h3>
            <p>Los dispositivos "multi-touch", como el iPad, se han vuelto en los últimos dos años cada vez más populares. Hoy en día no sólo se utilizan para navegar y enviar correos electrónicos, sino también como un medio para nuevos campos de aplicación. Algo particular de estos dispositivos (tablets), casi una necesidad, es la falta de retroalimentación "háptica" (percepción háptica), lo que hace que sea difícil establecer parámetros de uso con precisión. En otras palabras, una respuesta al tacto que aumente la experiencia de la interacción.</p>
