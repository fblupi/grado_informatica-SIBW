            <h1>Bases de Datos Multidimensionales, por E. Garví</h1>
            <p>Las bases de datos multidimensionales se utilizan principalmente para crear aplicaciones OLAP y pueden verse como bases de datos de una sola tabla, su peculiaridad es que por cada dimensión tienen un campo (o columna), y otro campo por cada métrica o hecho. Cada una de estas tablas puede asimilarse a un hipercubo o -más concretamente si de herramientas OLAP se trata- a un cubo OLAP, donde las dimensiones del mismo se corresponden los campos de dimensiones de la tabla, y el valor almacenado en cada celda del cubo equivale a la métrica o métricas almacenadas en la tabla.</p>
