            <h1>Procesadores de Lenguajes, por J. Revelles</h1>
            <p>El procesamiento de lenguajes naturales -abreviado PLN, o NLP del idioma inglés Natural Language Processing- es un campo de las ciencias de la computación, inteligencia artificial y lingüística que estudia las interacciones entre las computadoras y el lenguaje humano. El PLN se ocupa de la formulación e investigación de mecanismos eficaces computacionalmente para la comunicación entre personas y máquinas por medio de lenguajes naturales.</p>
