            <h1>Sistemas Unix/Linux</h1>
            <h3 id="ponente">por P. Paderewski</h3>
            <p>Unix (registrado oficialmente como UNIX&reg;) es un Sistema operativo portable, multitarea y multiusuario; desarrollado, en principio, en 1969, por un grupo de empleados de los laboratorios Bell de AT&amp;T, entre los que figuran Ken Thompson, Dennis Ritchie y Douglas McIlroy.
El sistema, junto con todos los derechos fueron vendidos por AT&amp;T a Novell, Inc. Esta vendió posteriormente el software a Santa Cruz Operation en 1995, y esta, a su vez, lo revendió a Caldera Software en 2001, empresa que después se convirtió en el grupo SCO. Sin embargo, Novell siempre argumentó que solo vendió los derechos de uso del software, pero que retuvo el copyright sobre "UNIX&reg;". En 2010, y tras una larga batalla legal, ésta ha pasado nuevamente a ser propiedad de Novell.</p>
